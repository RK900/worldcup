// One-off parser: turns the Wikipedia template wikitext into a static
// lookup table for FIFA's official 2026 WC third-place advancement
// assignment.
//
// Usage:
//   curl -s 'https://en.wikipedia.org/w/index.php?title=Template:2026_FIFA_World_Cup_third-place_table&action=raw' > /tmp/template.txt
//   node scripts/parse-third-place.mjs /tmp/template.txt > src/data/thirdPlaceTable.ts
//
// Wikipedia table layout per row (20 cells):
//   First 12 cells: one per group A..L. The bolded ones (e.g., '''E''')
//     mark the 8 advancing groups; blanks are the 4 not advancing.
//   Last 8 cells: third-place team assignment (e.g., 3E) for the 8
//     advancing slots, in Wikipedia's column order:
//       Wiki col index → 1A 1B 1D 1E 1G 1I 1K 1L
//   Our internal slot index (matchId order) → wiki col index:
//       slot 0 (M74, 1E v Best3) → wiki col 3
//       slot 1 (M77, 1I v Best3) → wiki col 5
//       slot 2 (M79, 1A v Best3) → wiki col 0
//       slot 3 (M80, 1L v Best3) → wiki col 7
//       slot 4 (M81, 1D v Best3) → wiki col 2
//       slot 5 (M82, 1G v Best3) → wiki col 4
//       slot 6 (M85, 1B v Best3) → wiki col 1
//       slot 7 (M87, 1K v Best3) → wiki col 6

import { readFileSync } from 'node:fs';

const SLOT_TO_WIKI_COL = [3, 5, 0, 7, 2, 4, 1, 6];

const path = process.argv[2];
if (!path) {
  console.error('usage: node scripts/parse-third-place.mjs <wikitext-file>');
  process.exit(1);
}
const raw = readFileSync(path, 'utf-8');
const lines = raw.split('\n');

const rows = [];
let i = 0;
while (i < lines.length) {
  const headerMatch = lines[i].match(/^! scope="row" \| (\d+)$/);
  if (!headerMatch) {
    i++;
    continue;
  }
  const rowNum = parseInt(headerMatch[1], 10);
  // The data line may be one cell-line, or split across two cell-lines
  // (for row 1, the rowspan column splits it). Collect cells from the
  // subsequent lines until we have 20.
  const cells = [];
  let j = i + 1;
  while (cells.length < 20 && j < lines.length) {
    const line = lines[j];
    if (!line.startsWith('|')) {
      // skip non-data lines (the rowspan header)
      j++;
      continue;
    }
    if (line.startsWith('|-')) {
      // separator — should not appear before we have 20 cells, but stop
      break;
    }
    // Strip leading '|' and split on '||', trim each, push.
    const segs = line.slice(1).split('||').map((s) => s.trim());
    cells.push(...segs);
    j++;
  }
  if (cells.length !== 20) {
    console.error(`Row ${rowNum}: expected 20 cells, got ${cells.length}`);
    process.exit(1);
  }
  const advancing = [];
  for (let k = 0; k < 12; k++) {
    const m = cells[k].match(/'''([A-L])'''/);
    if (m) advancing.push(m[1]);
  }
  if (advancing.length !== 8) {
    console.error(`Row ${rowNum}: expected 8 advancing groups, got ${advancing.length}: ${cells.slice(0, 12).join('|')}`);
    process.exit(1);
  }
  const wikiAssignments = [];
  for (let k = 12; k < 20; k++) {
    const m = cells[k].match(/3([A-L])/);
    if (!m) {
      console.error(`Row ${rowNum}: cell ${k} '${cells[k]}' not a 3X assignment`);
      process.exit(1);
    }
    wikiAssignments.push(m[1]);
  }
  const slotAssignments = SLOT_TO_WIKI_COL.map((wikiCol) => wikiAssignments[wikiCol]);
  rows.push({ rowNum, advancing, slotAssignments });
  i = j;
}

if (rows.length !== 495) {
  console.error(`Expected 495 rows, parsed ${rows.length}`);
  process.exit(1);
}

// Sanity check: each row's slotAssignments should be a permutation of advancing groups.
for (const r of rows) {
  const adv = [...r.advancing].sort().join('');
  const assigned = [...r.slotAssignments].sort().join('');
  if (adv !== assigned) {
    console.error(`Row ${r.rowNum}: advancing groups (${adv}) don't match slot assignments (${assigned})`);
    process.exit(1);
  }
}

// Emit TS source.
console.log('// Generated by scripts/parse-third-place.mjs from the Wikipedia');
console.log('// template "Template:2026 FIFA World Cup third-place table",');
console.log('// which mirrors FIFA Annex C. Do not edit by hand — regenerate');
console.log('// if FIFA ever updates the table.');
console.log('//');
console.log('// Key: 8 advancing groups concatenated in sorted order (e.g. "ABCDEFGH").');
console.log('// Value: tuple of 8 group letters indexed by our internal slot order');
console.log('//        [M74, M77, M79, M80, M81, M82, M85, M87].');
console.log('');
console.log('import type { GroupLetter } from "@/lib/types";');
console.log('');
console.log('export type ThirdPlaceAssignment = [');
console.log('  GroupLetter, GroupLetter, GroupLetter, GroupLetter,');
console.log('  GroupLetter, GroupLetter, GroupLetter, GroupLetter,');
console.log('];');
console.log('');
console.log('export const THIRD_PLACE_TABLE: Record<string, ThirdPlaceAssignment> = {');
for (const r of rows) {
  const key = [...r.advancing].sort().join('');
  const value = r.slotAssignments.map((g) => `"${g}"`).join(', ');
  console.log(`  ${key}: [${value}],`);
}
console.log('};');
